def get_ai_response(user_message: str) -> str:
    """–ü–æ–ª—É—á–∏—Ç—å –æ—Ç–≤–µ—Ç –Ω–∞ –≤–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è —Å –≤–æ—Ä–æ–Ω–∫–æ–π –∫ –∑–∞–∫–∞–∑—É."""
    message_lower = user_message.lower()
    
    matched_category = None
    response = None
    
    for category, data in KNOWLEDGE_BASE.items():
        for keyword in data["keywords"]:
            if keyword in message_lower:
                logger.info(f"AI matched category: {category}")
                matched_category = category
                response = data["response"]
                break
        if matched_category:
            break
    
    if not response:
        return DEFAULT_RESPONSE
    
    if any(word in message_lower for word in ["—Å—Ä–æ—á–Ω", "–∞–≤–∞—Ä–∏", "—Å–µ–π—á–∞—Å", "–Ω–µ–º–µ–¥–ª–µ–Ω", "–∑–∞—Ç–æ–ø"]):
        cta = PROBLEM_CTAS["urgent"]
    elif any(word in message_lower for word in ["—Ü–µ–Ω", "—Å—Ç–æ–∏–º", "—Å–∫–æ–ª—å–∫–æ", "–¥–æ—Ä–æ–≥–æ"]):
        cta = PROBLEM_CTAS["price"]
    elif any(word in message_lower for word in ["–Ω–µ –∑–Ω–∞—é", "–º–æ–∂–µ—Ç", "–Ω–∞–≤–µ—Ä–Ω", "–¥—É–º–∞—é"]):
        cta = PROBLEM_CTAS["doubt"]
    else:
        import random
        cta = random.choice(FUNNEL_CTAS)
    
    return response + cta
